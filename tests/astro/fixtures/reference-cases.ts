/**
 * Section 28 mandatory test case definitions.
 *
 * These define the required test inputs from calculations.md §28.
 * Independent reference values (from separate Jyotish software or JPL) are
 * marked TODO — do NOT invent values. When verified against an authoritative
 * source, replace TODO with the actual value and set verified: true.
 *
 * Status: PENDING — no independent reference values are verified yet.
 * Production gate: ASTRO_REAL_ENGINE_VALIDATION_REQUIRED must remain false
 * until all cases below have verified: true.
 */

export type ReferenceValue<T> =
  | { verified: true; value: T; source: string; verified_at: string }
  | { verified: false; value: null; source: 'TODO'; verified_at: null }

const TODO = <T>(): ReferenceValue<T> => ({
  verified: false, value: null, source: 'TODO', verified_at: null,
})

